    private void btnGirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGirarActionPerformed
   
    String rut = txtRut.getText().trim();
    String montoStr = txtSaldo.getText().trim();

    if (rut.isEmpty() || montoStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debes ingresar RUT y monto.");
        return;
    }

    long monto;
    try {
        monto = Long.parseLong(montoStr);
        if (monto <= 0) {
            JOptionPane.showMessageDialog(this, "El monto debe ser mayor que cero.");
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Monto invÃ¡lido.");
        return;
    }

    ClienteDAO clienteDAO = new ClienteDAO();
    Cliente cliente = clienteDAO.buscarPorRut(rut);
    if (cliente == null) {
        JOptionPane.showMessageDialog(this, "Cliente no encontrado.");
        return;
    }

    boolean pudoGirar = cliente.getCuenta().girar(monto);
    if (pudoGirar) {
       
        boolean exito = clienteDAO.actualizarSaldo(cliente.getCuenta().getNumeroCuenta(), cliente.getCuenta().getSaldo());
        if (exito) {
            JOptionPane.showMessageDialog(this, "Giro realizado correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "Error al actualizar el saldo en la base de datos.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "No hay saldo suficiente.");
    }  
    }//GEN-LAST:event_btnGirarActionPerformed

